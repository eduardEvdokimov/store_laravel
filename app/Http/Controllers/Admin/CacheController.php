    public function clear($item)
    {
        switch ($item) {
            case ('currencies') :
                \Currency::flush();
                break;
            case ('categories') :
                \Category::flush();
                break;
            case ('filters') :
                Filter::clearCache();
                break;
        }

        return back()->with(['success' => 'Кеш успешно очищен']);
    }

